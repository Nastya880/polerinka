def post_detail(request, year, month, day, post):
    post = get_object_or_404(Women, slug=post,
                             status='published',
                             publish__year=year,
                             publish__month=month,
                             publish__day=day)

    # List of active comments for this post
    comments = post.comments.filter(active=True)

    new_comment = None

    if request.method == 'POST':
        # A comment was posted
        comment_form = CommentForm(data=request.POST)
        if comment_form.is_valid():
            # Create Comment object but don't save to database yet
            new_comment = comment_form.save(commit=False)
            # Assign the current post to the comment
            new_comment.post = post
            # Save the comment to the database
            new_comment.save()
    else:
        comment_form = CommentForm()

    # List of similar posts
    post_tags_ids = post.tags.values_list('id', flat=True)
    similar_posts = Women.published.filter(tags__in=post_tags_ids) \
        .exclude(id=post.id)
    similar_posts = similar_posts.annotate(same_tags=Count('tags')) \
                        .order_by('-same_tags', '-publish')[:4]

    return render(request,
                  'blog/post/detail.html',
                  {'post': post,
                   'comments': comments,
                   'new_comment': new_comment,
                   'comment_form': comment_form,
                   'similar_posts': similar_posts})

class RatingView(View):

    def post(self, request, *args, **kwargs):
        post_id = request.POST.get('post')
        type = request.POST.get('type')
        women = Women.objects.get(id=post_id)
        created, _ = Rating.objects.update_or_create(user=request.user, post=women, defaults={'type': type})
        return JsonResponse({
            'value': '{} ({})'.format(
                dict(Rating.TYPE_CHOICES)[type],
                Rating.objects.filter(post=women, type=type).count(),
            ),
            'message': 'Вы успешно поставили {}'.format(
                dict(Rating.TYPE_CHOICES)[type]
            )
        })

    @method_decorator(csrf_exempt)
    def dispatch(self, request, *args, **kwargs):
        if not request.user.is_authenticated:
            return JsonResponse('Вы не авторизованы')
        return super().dispatch(request, *args, **kwargs)


